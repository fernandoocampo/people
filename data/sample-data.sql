CREATE TABLE IF NOT EXISTS people (
    ID VARCHAR(36) PRIMARY KEY,
    NAME VARCHAR(40) NOT NULL,
    CREATED_ON TIMESTAMP NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS pets (
    ID VARCHAR(36) PRIMARY KEY,
    NAME VARCHAR(40) NOT NULL,
    PERSON_ID VARCHAR(36) REFERENCES people,
    CREATED_ON TIMESTAMP NOT NULL DEFAULT NOW()
);